#!/bin/bash
# run-local.sh — Wrapper for running benchmarks locally on Docker Desktop (Windows/macOS).
#
# What it does:
#   - Fixes CRLF line endings and adds execute permissions to .sh files (WSL on /mnt/c/)
#   - Temporarily removes Linux-specific driver_opts from docker-compose.yaml volumes
#   - Generates docker-compose.override.yaml to disable node-exporter and alloy
#   - Delegates to run.sh, then restores original compose files on exit
#
# Usage: same as run.sh
#   ./run-local.sh -f "keccak" -t eest_tests -c nethermind -r 1

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
CLEANUP_FILES=()

cleanup() {
  for f in "${CLEANUP_FILES[@]}"; do
    if [[ "$f" == *.original ]]; then
      # Restore backed-up compose file
      target="${f%.original}"
      mv -f "$f" "$target" 2>/dev/null || true
    else
      # Remove generated override file
      rm -f "$f" 2>/dev/null || true
    fi
  done
}
trap cleanup EXIT INT TERM

# Fix CRLF and permissions on all shell scripts (needed when running from /mnt/c/ in WSL)
find "$SCRIPT_DIR" -name '*.sh' -exec sed -i 's/\r$//' {} +
find "$SCRIPT_DIR" -name '*.sh' -exec chmod +x {} +

# Clean up /tmp paths that Docker may have created as directories.
# The client run.sh scripts expect these to be files, but Docker creates
# missing host-mount paths as directories when the container starts first.
for p in /tmp/jwtsecret /tmp/chainspec.json /tmp/genesis.json /tmp/besu.json; do
  [ -d "$p" ] && rm -rf "$p" 2>/dev/null || true
done

# Patch each client's docker-compose.yaml
for compose_file in "$SCRIPT_DIR"/scripts/*/docker-compose.yaml; do
  client_dir="$(dirname "$compose_file")"

  # 1. Backup and remove driver_opts (Linux-only bind mount options)
  #    The driver_opts block is exactly 4 lines in all client compose files:
  #      driver_opts:
  #        type: none
  #        o: bind,rw,dirsync,noatime
  #        device: ${EC_DATA_DIR}
  cp "$compose_file" "$compose_file.original"
  CLEANUP_FILES+=("$compose_file.original")
  sed -i '/driver_opts:/,+3d' "$compose_file"

  # 2. Generate override to disable monitoring services (their volume mounts
  #    reference /:/host:ro,rslave which fails on Docker Desktop)
  override="$client_dir/docker-compose.override.yaml"
  cat > "$override" <<'YAML'
# Auto-generated by run-local.sh — do not commit
services:
  node-exporter:
    deploy:
      replicas: 0
  alloy:
    deploy:
      replicas: 0
YAML
  CLEANUP_FILES+=("$override")
done

echo "[run-local] Patched compose files for Docker Desktop compatibility"
echo "[run-local] Removed: driver_opts (Linux-only mount options)"
echo "[run-local] Disabled: node-exporter, alloy (monitoring, not needed locally)"
echo ""

# Delegate to run.sh (not exec, so cleanup trap fires on exit)
bash "$SCRIPT_DIR/run.sh" "$@"
